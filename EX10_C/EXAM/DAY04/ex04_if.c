#include <stdio.h>

// --------------------------------------------------------------
// 엔트피포인트 함수
// --------------------------------------------------------------
int main(void)
{
	// 지역변수 선언 및 초기화
	int num = 4;

	// 조건에 따른 출력 : 짝수여부에 따른 출력
	if (num % 2 == 0)
	{
		printf("%d 짝수\n", num);
	}

	//- 실행 코드가 1줄이면 블록{ } 생략 가능
	if (num % 2 == 0)
	printf("%d 짝수\n", num);

	if (num % 2 == 0) printf("%d 짝수\n", num);

	return 0;
}