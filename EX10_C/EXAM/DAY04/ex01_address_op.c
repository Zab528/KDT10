// --------------------------------------------------------------
// 라이브러리 로딩
// --------------------------------------------------------------
#include <stdio.h>


// --------------------------------------------------------------
// 엔트피포인트 함수
// --------------------------------------------------------------
int main(void)
{
	// 지역변수 선언 및 초기화
	int  x = 10;			// 데이터 10 저장
	int *p = &x;			// 변수 x의 주소 저장
	int* a = &x;

	// 변수와 데이터 출력
	printf("x가 저장하고 있는 데이터 = %d\n", x);
	printf("x의 주소                 = %p\n", &x);

	printf("p  : p가 저장하고 있는 주소             = %p\n", p);
	printf("*p : 저장하고 있는 주소의 데이터 읽기   = %d\n", *p);
	printf("&p : p의 주소                           = %p\n", &p);

	return 0;
}