// --------------------------------------------------------------
// Loading Library
// --------------------------------------------------------------
#include <stdio.h>

// --------------------------------------------------------------
// User Function Declaration
// --------------------------------------------------------------
int add(int x, int y);
int sub(int x, int y);
void multi(int x, int y);


// --------------------------------------------------------------
// [실습] 사용자 정의 함수 사용
// --------------------------------------------------------------
int main(void)
{
	// 지역변수 선언 및 초기화
	int num1 = 0, num2 = 10;

	// 함수호출
	int result = add(num1, num2);

	printf("add => %d\n", result);
	printf("sub => %d\n", sub(num1, num2) );


	multi(num1, num2);

	return 0;
}


// --------------------------------------------------------------
// 함수이름 : add
// 함수기능 : 2개 정수 덧셈 후 반환
// 매개변수 : int x, int y
// 반 환 값 : int 
// --------------------------------------------------------------
int add(int x, int y)
{
	return x + y;
}


// --------------------------------------------------------------
// 함수이름 : sub
// 함수기능 : 2개 정수 뺄셈 후 반환
// 매개변수 : int x, int y
// 반 환 값 : int 
// --------------------------------------------------------------
int sub(int x, int y)
{
	return x - y;
}


// --------------------------------------------------------------
// 함수이름 : multi
// 함수기능 : 2개 정수 곱셈 후 결과 출력
// 매개변수 : int x, int y
// 반 환 값 : void
// --------------------------------------------------------------
void multi(int x, int y)
{
	printf("%d x %d = %d\n", x, y, x * y);
}