// --------------------------------------------------------------
#include <stdio.h>


// --------------------------------------------------------------
// [실습] while 반복문 기본 형식 이해
// --------------------------------------------------------------

int main(void)
{
	int sum = 0, i = 0;

	while ((sum += ++i) <= 100)
		printf("%d-%d\n", i, sum);
	return 0;
}