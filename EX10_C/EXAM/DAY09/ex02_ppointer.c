#include <stdio.h>

// --------------------------------------------------------------
// Entry Point Function
// --------------------------------------------------------------
int main(void)
{
	// 다양한 배열 : 동일한 타입 !!
	int n1 = 100, n2 = 200, n3 = 300;
	int nums[] = { 10,20,30 };
	int* pArr[3];

	// 배열 초기화
	pArr[0] = &n1;
	pArr[1] = &n2;
	pArr[2] = &n3;

	// 배열 요소 읽기
	for (int idx = 0; idx < 3; idx++)
	{
		printf("[%d] - %p, %d\n", idx, pArr[idx], *pArr[idx]);
	}

	return 0;
}